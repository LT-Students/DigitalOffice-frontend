/**
 * Specific position data.
 */
export interface Position {

  /**
   * Position description.
   */
  description?: null | string;

  /**
   * Specific position id.
   */
  id?: string;

  /**
   * Marks whether position is active or not.
   */
  isActive?: boolean;

  /**
   * Position name.
   */
  name?: string;
}

